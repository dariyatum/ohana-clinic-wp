<section class="director-career">
  <h2>Career & Professional Background</h2>
  <p>
    After graduating from Keio University School of Medicine in 1983, Dr. Takehara has been at the forefront of IVF
    treatment in Japan since 1989. He was the attending physician for one of the first successful pregnancies and
    births using frozen embryos in Japan. Dr. Takehara also conducted research on ovulation mechanisms and genetic
    testing of embryos in the United States. Upon his return to Japan, he provided IVF, ICSI, obstetrics, and gynecology
    care. From 2003 to 2012 he served as Deputy Director at Kato Ladies Clinic, performing over 22,000 IVF/ICSI cycles.
    In March 2013, he established Keiai Clinic and has since been involved in over 192,000 IVF procedures in his 34-year career.
    In addition, as a clinical genetic specialist he has played a significant role in prenatal diagnosis practices in Japan.
